    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        String kodeSup = txt_kodeSupp.getText();
        String namaSup = txt_NamaSupp.getText();
        String alamat = txt_AlamatSupp.getText();
        String kota = txt_Kota.getText();
        String negara = txt_Negara.getText();
        String noTelp = txt_NoTelp.getText();
        String email = txt_Email.getText();
        String noRek = txt_NoRek.getText();
        String npwp = txt_NPWP.getText();
        String provinsi = txt_Prov.getText();
        String kodePos = txt_KodePos.getText();

        String query = "INSERT INTO supplier (kode_supplier, nama, alamat, kota, negara, noTelp, email, noRek, npwp, provinsi, kodePos) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)";

        try {
            conn = Config.configDB();
            PreparedStatement pstmt = conn.prepareStatement(query);
            pstmt.setString(1, kodeSup);
            pstmt.setString(2, namaSup);
            pstmt.setString(3, alamat);
            pstmt.setString(4, kota);
            pstmt.setString(5, negara);
            pstmt.setString(6, noTelp);
            pstmt.setString(7, email);
            pstmt.setString(8, noRek);
            pstmt.setString(9, npwp);
            pstmt.setString(10, provinsi);
            pstmt.setString(11, kodePos);
            pstmt.executeUpdate();
            conn.close(); // Close the connection
            JOptionPane.showMessageDialog(null, "berhasil yeayy");
        } catch (Exception err) {
            JOptionPane.showMessageDialog(null, err.getMessage());
        }
    }//GEN-LAST:event_jButton5ActionPerformed

