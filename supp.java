    private void loadData() {
        DefaultTableModel tableModel = new DefaultTableModel();
        //utk memuat judul kolom tabel
        tableModel.addColumn("Kode");
        tableModel.addColumn("Nama");
        tableModel.addColumn("Alamat");
        tableModel.addColumn("Kota");
        tableModel.addColumn("NoRek");
        tableModel.addColumn("NPWP");
        tableModel.addColumn("Provinsi");
        tableModel.addColumn("KodePos");
        tableModel.addColumn("Negara");
        tableModel.addColumn("NoTelp");
        tableModel.addColumn("Email");
        try {
            String sql = "SELECT * FROM supplier";
            conn = Config.configDB();
            stat = conn.createStatement();
            ResultSet results = stat.executeQuery(sql);

            while (results.next()) {
                tableModel.addRow(new String[]{
                    results.getString("kode_supplier"),
                    results.getString("nama"),
                    results.getString("alamat"),
                    results.getString("kota"),
                    results.getString("noRek"),
                    results.getString("npwp"),
                    results.getString("provinsi"),
                    results.getString("kodePos"),
                    results.getString("negara"),
                    results.getString("noTelp"),
                    results.getString("email"),});
            }
            TblSup.setModel(tableModel);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "EROR" + e.getMessage());
        }
    }

